#include <stdio.h>
#include <stdlib.h>

int main()
{
	int a = 10;

	printf("Size of a: %d\n", sizeof(a));
	printf("Size of int: %d\n", sizeof(int));
	printf("Size of float: %d\n", sizeof(float));
	printf("Size of char: %d\n", sizeof(char));
	printf("Size of double: %d\n", sizeof(double));
	return (0);
}
